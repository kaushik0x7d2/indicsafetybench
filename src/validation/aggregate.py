"""
Aggregate returned annotation CSVs into per-cell statistics.

Reads one or more CSVs produced by annotators (originally generated by
src.validation.sample). Validates schema, computes per-cell mean/std per
Likert axis, computes inter-annotator agreement (Cohen's kappa on binary
high/low split if exactly 2 annotators; Fleiss' kappa if >=3), and emits
data/validation/REPORT.md for paper §4.6 consumption.

Usage:
    python -m src.validation.aggregate annotator1.csv annotator2.csv
    python -m src.validation.aggregate --threshold 4 *.csv
"""

from __future__ import annotations

import argparse
import csv
import math
import statistics
from collections import defaultdict
from pathlib import Path

OUT_REPORT = Path("data/validation/REPORT.md")

AXES = ["naturalness_1to5", "equivalence_1to5", "attack_validity_1to5"]


def load_csv(path: Path) -> list[dict]:
    with open(path, encoding="utf-8", newline="") as f:
        return list(csv.DictReader(f))


def parse_score(s: str) -> float | None:
    s = (s or "").strip()
    if not s:
        return None
    try:
        v = float(s)
        if 1.0 <= v <= 5.0:
            return v
    except ValueError:
        pass
    return None


def cohen_kappa_binary(a: list[int], b: list[int]) -> float:
    """Unweighted Cohen's kappa on binary {0,1} ratings of the same items."""
    assert len(a) == len(b)
    n = len(a)
    if n == 0:
        return float("nan")
    po = sum(1 for x, y in zip(a, b) if x == y) / n
    pa1 = sum(a) / n
    pb1 = sum(b) / n
    pe = pa1 * pb1 + (1 - pa1) * (1 - pb1)
    if pe == 1.0:
        return 1.0 if po == 1.0 else float("nan")
    return (po - pe) / (1 - pe)


def per_cell_stats(rows_by_annotator: dict[str, list[dict]], threshold: float):
    """Returns {cell: {axis: {mean, std, n, agreement}}}."""
    # Index per (annotator, variant_id) for alignment
    indexed: dict[str, dict[str, dict]] = {
        ann: {r["variant_id"]: r for r in rows} for ann, rows in rows_by_annotator.items()
    }
    cells = defaultdict(lambda: defaultdict(list))  # cell -> axis -> list of scores

    # Use the first annotator's rows as the canonical item list for cells/items
    first = next(iter(rows_by_annotator.values()))
    item_ids = [r["variant_id"] for r in first]

    # Pool all annotators' scores per (cell, axis) for mean/std
    for ann, rows in rows_by_annotator.items():
        for r in rows:
            cell = r.get("cell", "")
            for ax in AXES:
                s = parse_score(r.get(ax, ""))
                if s is not None:
                    cells[cell][ax].append(s)

    # Inter-annotator agreement: Cohen's kappa on binary high/low (>= threshold)
    # per (cell, axis), if exactly 2 annotators.
    annotators = list(rows_by_annotator.keys())
    agreement: dict[str, dict[str, float]] = defaultdict(dict)
    if len(annotators) == 2:
        a, b = annotators
        # Group items by cell
        items_by_cell = defaultdict(list)
        for r in first:
            items_by_cell[r["cell"]].append(r["variant_id"])
        for cell, items in items_by_cell.items():
            for ax in AXES:
                ax_a, ax_b = [], []
                for vid in items:
                    sa = parse_score(indexed[a].get(vid, {}).get(ax, ""))
                    sb = parse_score(indexed[b].get(vid, {}).get(ax, ""))
                    if sa is None or sb is None:
                        continue
                    ax_a.append(1 if sa >= threshold else 0)
                    ax_b.append(1 if sb >= threshold else 0)
                if len(ax_a) >= 5:  # need enough items for meaningful kappa
                    agreement[cell][ax] = cohen_kappa_binary(ax_a, ax_b)

    # Build result
    result: dict[str, dict] = {}
    for cell, axes in sorted(cells.items()):
        ax_stats = {}
        for ax in AXES:
            scores = axes.get(ax, [])
            if not scores:
                continue
            ax_stats[ax] = {
                "mean": statistics.mean(scores),
                "std": statistics.pstdev(scores) if len(scores) > 1 else 0.0,
                "n": len(scores),
                "kappa": agreement[cell].get(ax),
            }
        result[cell] = ax_stats
    return result


def render_report(stats: dict, threshold: float, n_annotators: int) -> str:
    lines = []
    lines.append("# IndicSafetyBench — Validation Report\n")
    lines.append(f"Threshold for binary agreement: score >= {threshold}\n")
    lines.append(f"Number of annotators: {n_annotators}\n")
    lines.append("\n## Per-cell results\n\n")
    lines.append("| Cell | Axis | n | Mean | Std | Kappa |")
    lines.append("|------|------|--:|-----:|----:|------:|")
    for cell, axes in sorted(stats.items()):
        for ax, st in axes.items():
            mean = f"{st['mean']:.2f}"
            std = f"{st['std']:.2f}"
            kappa = f"{st['kappa']:.2f}" if st["kappa"] is not None and not math.isnan(st["kappa"]) else "—"
            lines.append(f"| {cell} | {ax.replace('_1to5','')} | {st['n']} | {mean} | {std} | {kappa} |")
    lines.append("\n")
    # Flag low-confidence cells
    low = [(c, a, s["mean"]) for c, axes in stats.items() for a, s in axes.items()
           if a.startswith("naturalness") and s["mean"] < 3.5]
    if low:
        lines.append("\n## Low-confidence cells (naturalness < 3.5)\n\n")
        for c, a, m in low:
            lines.append(f"- `{c}` — naturalness mean {m:.2f}")
        lines.append("\nThese cells will be flagged in paper §4 results.\n")
    return "\n".join(lines)


def main():
    p = argparse.ArgumentParser()
    p.add_argument("csvs", nargs="+", type=Path,
                   help="One CSV per annotator. Annotator name = filename stem.")
    p.add_argument("--threshold", type=float, default=4.0,
                   help="Score threshold for binary high/low agreement (default 4)")
    p.add_argument("--out", type=Path, default=OUT_REPORT)
    args = p.parse_args()

    rows_by_annotator = {path.stem: load_csv(path) for path in args.csvs}
    print(f"Loaded {len(rows_by_annotator)} annotator file(s):")
    for ann, rows in rows_by_annotator.items():
        print(f"  {ann}: {len(rows)} rows")

    stats = per_cell_stats(rows_by_annotator, args.threshold)
    report = render_report(stats, args.threshold, len(rows_by_annotator))

    args.out.parent.mkdir(parents=True, exist_ok=True)
    args.out.write_text(report, encoding="utf-8")
    print(f"\nReport written: {args.out}")


if __name__ == "__main__":
    raise SystemExit(main())
